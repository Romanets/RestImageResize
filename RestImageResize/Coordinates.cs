namespace RestImageResize
{
    /// <summary>
    /// Represents the coordinates of point inside the image
    /// </summary>
    public struct Coordinates
    {
        /// <summary>
        /// Gets or sets the X coordinate of point
        /// </summary>
        public int X { get; set; }

        /// <summary>
        /// Gets or sets the Y coordinate of point
        /// </summary>
        public int Y { get; set; }
    }
}